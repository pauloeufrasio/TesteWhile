public class TesteWhile {
    public static void main(String[] args) {
        // Inicializando a variavel contador
        int contador = 0;
        // enquanto contador for menor igual a 10 ele irá contar a ate 10
        while (contador <= 10) {
            System.out.println(contador);
        // Incrementando o que está dentro dele mais um
            contador++;
        }

    }
}
